package Schildt.glava1;

public class LunarMass {
    public static void main(String args[]){
        double lun, ear;

        ear = 76.51;

        System.out.println("Ваша масса на земле составляет: " + ear);
        System.out.println();

        lun = ear * 9.806 * 0.17;

        System.out.println("Тогда ваш вес на луне составит: " + lun);
    }
}
